#Crie um programa que calcule a area de um trapézio
base1 = float(input("Digite o valor da base menor : \n"))
base2 = float(input("Digite o valor da base maior : \n"))
altura = float(input("Digite o valor da altura : \n"))
#Calcule a area do trapézio
area = ((base1+base2)*altura)/2
print("A area do trapézio é : ",area)
